from functions import cat

#==================================================================================================#
# Clears ####
cat('Clears', 'green').print()
#--------------------------------------------------------------------------------------------------#
from os import remove
from shutil import rmtree
from os.path import exists
#--------------------------------------------------------------------------------------------------#

log_file = 'log.txt'
boxplot_cv_file = 'cv_models.png'
confusion_matrix_file = 'confusion_matrix.png'

#==========================================================#
cat('Del Py Cache').print()
#----------------------------------------------------------#
if exists('./Python/__pycache__'):
  rmtree('./Python/__pycache__')
#==========================================================#

#==========================================================#
cat('Del Boxplots').print()
#----------------------------------------------------------#
if exists(boxplot_cv_file):
  remove(boxplot_cv_file)
#==========================================================#

#==========================================================#
cat('Del Confusion Matrix').print()
#----------------------------------------------------------#
if exists(confusion_matrix_file):
  remove(confusion_matrix_file)
#==========================================================#

#==========================================================#
cat('Del Log File').print()
#----------------------------------------------------------#
if exists(log_file):
  remove(log_file)
#==========================================================#

#==========================================================#
cat('Del .Rhistory File').print()
#----------------------------------------------------------#
if exists('.Rhistory'):
  remove('.Rhistory')
#==========================================================#

